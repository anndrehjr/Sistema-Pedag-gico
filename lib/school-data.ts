// Função para classificar notas pedagogicamente
export function classificarNota(nota: number): string {
  if (nota <= 4) return "Abaixo do Básico"
  if (nota <= 6) return "Básico"
  if (nota <= 8) return "Adequado"
  return "Avançado"
}

// Função para obter cor da classificação
export function getCorClassificacao(classificacao: string): string {
  switch (classificacao) {
    case "Abaixo do Básico":
      return "bg-red-100 text-red-700 border-red-200"
    case "Básico":
      return "bg-amber-100 text-amber-700 border-amber-200"
    case "Adequado":
      return "bg-blue-100 text-blue-700 border-blue-200"
    case "Avançado":
      return "bg-emerald-100 text-emerald-700 border-emerald-200"
    default:
      return "bg-gray-100 text-gray-700 border-gray-200"
  }
}

// Função para calcular média
function calcularMedia(notas: Record<string, number | "TR">): number {
  const valores = Object.values(notas).filter((n): n is number => typeof n === "number")
  if (valores.length === 0) return 0
  return Number((valores.reduce((a, b) => a + b, 0) / valores.length).toFixed(1))
}

