// Funções de estatísticas
export function getEstatisticasGerais() {
  const todasTurmas = escolaData.turmas
  const todosAlunos = todasTurmas.flatMap((t) => t.alunos)
  const totalAlunos = todosAlunos.length
  const totalTurmas = todasTurmas.length
  const alunosCriticos = todosAlunos.filter((a) => a.media < 5).length
  const mediaGeral = Number((todosAlunos.reduce((acc, a) => acc + a.media, 0) / totalAlunos).toFixed(1))

  // Melhor e pior turma
  const turmasComMedia = todasTurmas.map((t) => ({
    ...t,
    mediaGeral: Number((t.alunos.reduce((acc, a) => acc + a.media, 0) / t.alunos.length).toFixed(1)),
  }))

  const melhorTurma = turmasComMedia.reduce((a, b) => (a.mediaGeral > b.mediaGeral ? a : b))
  const piorTurma = turmasComMedia.reduce((a, b) => (a.mediaGeral < b.mediaGeral ? a : b))

  return {
    totalAlunos,
    totalTurmas,
    alunosCriticos,
    mediaGeral,
    melhorTurma: { nome: melhorTurma.nome, media: melhorTurma.mediaGeral },
    piorTurma: { nome: piorTurma.nome, media: piorTurma.mediaGeral },
  }
}

export function getDistribuicaoPedagogica(alunos: Aluno[]) {
  const distribuicao = {
    "Abaixo do Básico": 0,
    Básico: 0,
    Adequado: 0,
    Avançado: 0,
  }

  alunos.forEach((aluno) => {
    if (aluno.classificacao in distribuicao) {
      distribuicao[aluno.classificacao as keyof typeof distribuicao]++
    }
  })

  return Object.entries(distribuicao).map(([name, value]) => ({
    name,
    value,
    fill:
      name === "Abaixo do Básico"
        ? "var(--chart-1)"
        : name === "Básico"
          ? "var(--chart-2)"
          : name === "Adequado"
            ? "var(--chart-3)"
            : "var(--chart-4)",
  }))
}

export function getMediaPorMateria(turma: Turma) {
  return turma.materias.map((materia) => {
    const notas = turma.alunos
      .map((a) => a.notas[materia])
      .filter((n): n is number => typeof n === "number")
    const media = notas.length > 0 ? Number((notas.reduce((a, b) => a + b, 0) / notas.length).toFixed(1)) : 0
    return {
      materia: materia.length > 15 ? materia.substring(0, 15) + "..." : materia,
      materiaCompleta: materia,
      media,
      classificacao: classificarNota(media),
    }
  })
}

export function getAlunosEmAtencao() {
  return escolaData.turmas.flatMap((turma) =>
    turma.alunos
      .filter((a) => a.atencao || a.media < 5)
      .map((aluno) => {
        const materiasAbaixo = Object.entries(aluno.notas)
          .filter(([, nota]) => typeof nota === "number" && nota < 5)
          .map(([materia]) => materia)
        return {
          ...aluno,
          turma: turma.nome,
          turmaId: turma.id,
          materiasAbaixo,
        }
      })
  )
}
